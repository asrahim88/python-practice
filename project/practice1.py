
#addition of two numbers
def sum_of_two_numbers(x, y):
    return x + y

# substraciton of two numbers.
def sub_of_two_numbers(x, y):
    return x - y

# multiplication of two numbers
def multiplication_of_numbers(x, y):
    return x * y

# division of two numbers
def division_of_two_numbers(x , y):
    return x / y

# exponentiation of two numbers
def exponentiation_of_two_number(x, y):
    return x // y

#modulus of two numbers
def modulus_of_two_numbers(x, y):
    return x % y

#power of two numbers
def power_of_two_numbers(x, y):
    return x ** y
